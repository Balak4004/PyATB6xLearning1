
# Find maximum value from dict


def max_value_dict(dict1):
    return max(dict1.values())

print(max_value_dict({"a":10,"b":20,"c":30}))

def min_value_dict(dict1):
    return min(dict1.values())

print(min_value_dict({"a":10,"b":20,"c":30}))

def max_key_dict(dict1):
    return max(dict1.keys())

print(max_key_dict({"a":10,"b":20,"c":30}))

def min_key_dict(dict1):
    return min(dict1.keys())

print(min_key_dict({"a":10,"b":20,"c":30}))